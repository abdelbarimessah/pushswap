#include<stdio.h>

int main()
{
	int a[] = {3333,5,0,220,5454,000,22226,64,64,64,64,50,66,1};
	int i;
	int j;
	int x;
	
	i = 1;
	while(i < 14 )
	{
		x = a[i];
		j = i - 1;
		while(j >= 0 && a[j] > x)
		{
			a[j + 1] = a[j];
			j--;
		}
		a[j + 1] = x;
		i++;
	}
	i = 0;
	while(i < 14)
	{
		printf("%d\n",a[i]);
		i++;
	}
}
